    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        ClienteController cc = new ClienteController();
        Cliente c = new Cliente();
        
        PaisController pc = new PaisController();
        Pais p = new Pais();
        
        p = pc.ler(Integer.parseInt(cmbPaises.getSelectedItem().toString()));
        
        try {
            c.setNome(jlNome.getText());
            c.setTelefone(jlTelefone.getText());
            c.setIdade(Integer.parseInt(jlIdade.getText()));
            c.setPais(p);
            c.setLimite_credito(0);
        } catch (Exception ex) {
            Logger.getLogger(TelaAlterarCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        cc.alterar(c, Integer.parseInt(jlIdASerAlterado.getText()));
    }//GEN-LAST:event_jbAlterarActionPerformed

